package examples.threading;

/**
 * Simple thread target that runs an infinite while-loop
 * 
 * @author gerberma
 * 
 */
public class WhileLoopRunner implements Runnable
{
    /**
     * Runs an infinite while-loop
     */
    @Override
    public void run()
    {
        // go!
        while (true)
        {
        }
    }
}
